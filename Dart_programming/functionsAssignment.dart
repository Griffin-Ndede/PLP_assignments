void addTwo(int num1, int num2) {
  int sum = num1 + num2;
  print(sum);
}

void subtractTwo(int num1, int num2) {
  int result = num1 - num2;
  print(result);
}

void multiplyTwo(int num1, int num2) {
  int result = num1 * num2;
  print(result);
}

void main() {
  addTwo(10, 5);
  subtractTwo(5, 9);
  multiplyTwo(20, 30);
}
